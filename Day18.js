function drawClock(time) {
    const digitPatterns = [
        [
            ['*', '*', '*'],
            ['*', ' ', '*'],
            ['*', ' ', '*'],
            ['*', ' ', '*'],
            ['*', ' ', '*'],
            ['*', ' ', '*'],
            ['*', '*', '*']
        ],
        [
            [' ', ' ', '*'],
            [' ', ' ', '*'],
            [' ', ' ', '*'],
            [' ', ' ', '*'],
            [' ', ' ', '*'],
            [' ', ' ', '*'],
            [' ', ' ', '*']
        ],
        [
            ['*', '*', '*'],
            [' ', ' ', '*'],
            [' ', ' ', '*'],
            ['*', '*', '*'],
            ['*', ' ', ' '],
            ['*', ' ', ' '],
            ['*', '*', '*']
        ],
        [
            ['*', '*', '*'],
            [' ', ' ', '*'],
            [' ', ' ', '*'],
            ['*', '*', '*'],
            [' ', ' ', '*'],
            [' ', ' ', '*'],
            ['*', '*', '*']
        ],
        [
            ['*', ' ', '*'],
            ['*', ' ', '*'],
            ['*', ' ', '*'],
            ['*', '*', '*'],
            [' ', ' ', '*'],
            [' ', ' ', '*'],
            [' ', ' ', '*']
        ],
        [
            ['*', '*', '*'],
            ['*', ' ', ' '],
            ['*', ' ', ' '],
            ['*', '*', '*'],
            [' ', ' ', '*'],
            [' ', ' ', '*'],
            ['*', '*', '*']
        ],
        [
            ['*', '*', '*'],
            ['*', ' ', ' '],
            ['*', ' ', ' '],
            ['*', '*', '*'],
            ['*', ' ', '*'],
            ['*', ' ', '*'],
            ['*', '*', '*']
        ],
        [
            ['*', '*', '*'],
            [' ', ' ', '*'],
            [' ', ' ', '*'],
            [' ', ' ', '*'],
            [' ', ' ', '*'],
            [' ', ' ', '*'],
            [' ', ' ', '*']
        ],
        [
            ['*', '*', '*'],
            ['*', ' ', '*'],
            ['*', ' ', '*'],
            ['*', '*', '*'],
            ['*', ' ', '*'],
            ['*', ' ', '*'],
            ['*', '*', '*']
        ],
        [
            ['*', '*', '*'],
            ['*', ' ', '*'],
            ['*', ' ', '*'],
            ['*', '*', '*'],
            [' ', ' ', '*'],
            [' ', ' ', '*'],
            ['*', '*', '*']
        ]
    ];
    function drawColon() {
        return [
            [' ', ' ', ' '],
            [' ', ' ', ' '],
            [' ', '*', ' '],
            [' ', ' ', ' '],
            [' ', '*', ' '],
            [' ', ' ', ' '],
            [' ', ' ', ' ']
        ];
    }
    function drawSpace() {
        return [
            [' '],
            [' '],
            [' '],
            [' '],
            [' '],
            [' '],
            [' ']
        ];
    }

    function getDigitPattern(digit) {
        return digitPatterns[digit];
    }
    const first = parseInt(time[0]);
    const second = parseInt(time[1]);
    const third = parseInt(time[3]);
    const fourth = parseInt(time[4]);

    const spacedArrays = [
        getDigitPattern(first),
        drawSpace(),
        getDigitPattern(second),
        drawColon(),
        getDigitPattern(third),
        drawSpace(),
        getDigitPattern(fourth)
    ];
    const result = spacedArrays[0].map((_, rowIndex) => {
        return spacedArrays.map(array => array[rowIndex]).flat();
    });
    return result;
}

